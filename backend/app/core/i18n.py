def translate(key: str, request: Request) -> str:
    # Tarayıcının gönderdiği dili oku, varsayılan TR
    accept_language = request.headers.get("Accept-Language", "tr").lower()
    
    lang = "tr"
    if "en" in accept_language:
        lang = "en"
    elif "de" in accept_language:
        lang = "de"
    elif "ar" in accept_language:
        lang = "ar"
        
    return TRANSLATIONS.get(lang, TRANSLATIONS["tr"]).get(key, f"[{key}]")

_ = translate
